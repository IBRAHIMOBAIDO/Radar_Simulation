void MovingServo_RL(int degree)
{
	 	 		for(int i= degree + 44; i<= degree + 45; i++) //from zero degree too 180 degree
	 	 			 	  	  {
	 	 			 	  		  TIM4->CCR1 = i;
	 	 			 	  		  HAL_Delay(10);
	 	 			 	  	  }
}
void MovingServo_LR(int degree)
{
	 	 		for(int i= degree + 45; i>= degree + 44; i--) //from 180 degree too zero degree
	 	 			 	  	  {
	 	 			 	  		  TIM4->CCR1 = i;
	 	 			 	  		  HAL_Delay(10);
	 	 			 	  	  }
}

